package com.example.demo.test;



public class CircleArea {
    public static void main(String[] args) {
        // 定义圆的半径
        double radius = 5.0;

        // 计算圆的面积
        double area = Math.PI * Math.pow(radius, 2);

        // 输出结果
        System.out.println("圆的半径是: " + radius);
        System.out.println("圆的面积是: " + area);
    }
}
